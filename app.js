let x, y, z;

function drawPlayground (x,y,z) {
    switch(z){
        case 1:
            ctx.fillStyle = "grey";
            ctx.fillRect(x, y, b, b);
            break;
        case 2:
            let img5 = document.getElementById("dot");
            ctx.drawImage(img5,x+15,y+15);
            break; 
        case 3:
            let img = document.getElementById("pac");
            ctx.drawImage(img,x,y);
            break;   
            
        case 4:
            let img2 = document.getElementById("blue");
            ctx.drawImage(img2,x,y);
            break;   
        case 5:
            let img3 = document.getElementById("yellow");
            ctx.drawImage(img3,x,y);
            break;   
        case 6:
            let img4 = document.getElementById("red");
            ctx.drawImage(img4,x,y);
            break; 
            
        default:
            ctx.fillStyle = "white";
            ctx.fillRect(x, y, b, b);
     }       

}


